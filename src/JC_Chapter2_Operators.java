public class JC_Chapter2_Operators {
    public static void main(String[] args) {
        //Arithmetic operators
        int a = 10;
        int b = 45-a;
        int c = 53%a;
        System.out.println("Value of b is: "+b);
        System.out.println("Value of c is: "+c);

        //Assignment Operator
        int d=7;
        d *=3;
        int e =8;
        e +=5;
        System.out.println("value of d is :"+d);
        System.out.println("Value of e is "+e);

        //Comparison operator
        System.out.println(64<6);
        System.out.println(838>87837);

        //Logical Operator
        System.out.println(64>5 && 64<676);
        System.out.println(737<6834 || 83<9);



    }
}
